package com.mydemo.test31.data;

public class OptionItem {
    private int id;
    private String name;

    public OptionItem(int id, String name) {
        this.id = id;
        this.name = name;
    }

    // Getter
    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}